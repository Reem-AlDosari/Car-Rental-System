    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // searchAvailableEconomy
        
        Economy[] arr= rental.searchAvailableEconomy() ; 
        
        if( arr == null)
        {
          jTextArea1.setText("there ar no any available Economy Car ");
          return ; 
        }
        else
         jTextArea1.setText("");
        for( int i = 0 ; i < arr.length ; i++)
        {
            if( arr[i] != null )
                jTextArea1.append(arr[i].toString());
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // show Available  Vip
        
        VIP[] arr= rental.searchAvailableVIP(); 
        
        if( arr == null)
        {
          jTextArea1.setText("there ar no any available VIP Car ");
          return ; 
        }
        else
         jTextArea1.setText("");
        for( int i = 0 ; i < arr.length ; i++)
        {
            if( arr[i] != null ){
            jTextArea1.append(arr[i].toString());
            jTextArea1.append(  "Driver : " +  ( (VIP)arr[i] ).getDriver().toString()   );
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Rent car 
        int NoDays = 0 ; 
        String plateNo  = ""; 
        try{
         plateNo = jTextField7.getText() ; 
         NoDays = Integer.parseInt( jTextField8.getText() ) ; 
        
        if( plateNo.length() <= 3 )
        {
            JOptionPane.showMessageDialog(this, "plate number must be more than 3 characters ");
            return ; 
        }
        }
        catch(NumberFormatException ex )
        {
            JOptionPane.showMessageDialog(this, "NOof days should be integer only");
            return ; 
        }
        
       
        
        // read customer info 
        Customer  cr = null; 
        try{
        int id =Integer.parseInt( jTextField9.getText() ) ; 
        String name = jTextField10.getText() ; 
        long phone = Long.parseLong(jTextField11.getText() ) ; 
        cr = new Customer(id , name , phone ) ; 
        if( name.length() <= 2 )
        {
            JOptionPane.showMessageDialog(this, "name should be more than 2  characters ");
            return ; 
        }
        }
        catch(NumberFormatException ex )
        {
            JOptionPane.showMessageDialog(this, "id and phone for customer should be integers only");
            return ; 
        }
     
        rental.rentCar(plateNo, cr, NoDays);
        
        jTextField7.setText("");
        jTextField8.setText("1");
        jTextField9.setText("");
        jTextField10.setText("");
        jTextField11.setText("");
        
    }//GEN-LAST:event_jButton4ActionPerformed

