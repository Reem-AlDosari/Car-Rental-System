    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // add new Car 
        try{
      String plateNo = jTextField1.getText();  
      double price = Double.parseDouble(  jTextField2.getText()  ) ; 
      String model = jTextField3.getText() ; 
      String color = jTextField4.getText() ; 
      
      if( plateNo.length() <= 2 || model.length() <= 2  || color.length() <= 2  )
      {
          JOptionPane.showMessageDialog(this, "the length of input must contain more than 2 char ");
          return ;
      }
      
      if( price <= 50 )
      {
         JOptionPane.showMessageDialog(this, "the price should be more than 50 SR per Day ");
          return ;  
      }
      
      if( jRadioButton1.isSelected() == true )  // Economy
      {
          Economy eco = new Economy(plateNo , price , model , color ) ;
          rental.addCar(eco);
      }
      else
          if( jRadioButton2.isSelected() == true ) // Vip
      {
           int id = Integer.parseInt(jTextField5.getText()) ; 
           String name = jTextField6.getText() ;
           
            if( name.length() <= 2 || id == 0   )
      {
          JOptionPane.showMessageDialog(this, "the length of input must contain more than 2 char ");
          return ;
      }
           Driver d = new Driver(id , name ) ; 
          VIP vip = new VIP(plateNo , price , model , color , d ) ;
          rental.addCar(vip);
      }
      
        }catch( NumberFormatException ex )
        {
           JOptionPane.showMessageDialog(this, "Error  input numbers ");
           return ; 
        }
      jTextField1.setText(""); 
      jTextField2.setText("");
       jTextField3.setText(""); 
      jTextField4.setText(""); 
      jTextField5.setText("");
       jTextField6.setText(""); 
        
    }//GEN-LAST:event_jButton1ActionPerformed

